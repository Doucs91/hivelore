const CI_WORKFLOW = `name: haive-sync

on:
  push:
    branches: [main, master]
  pull_request:
    branches: [main, master]

jobs:
  # On push to main/master: sync anchors + auto-promote + commit changes
  sync-on-merge:
    if: github.event_name == 'push'
    runs-on: ubuntu-latest
    permissions:
      contents: write
    steps:
      - uses: actions/checkout@v4
        with:
          fetch-depth: 0

      - uses: actions/setup-node@v4
        with:
          node-version: '20'

      - name: install haive
        run: npm install -g @hiveai/cli

      - name: refresh memory anchors + auto-promote
        run: haive sync --since HEAD~1 || true

      - name: commit updated memories (if any)
        run: |
          git config user.name "github-actions[bot]"
          git config user.email "github-actions[bot]@users.noreply.github.com"
          git add .ai/
          git diff --cached --quiet || git commit -m "chore: haive sync [skip ci]"
          git push

  # On pull request: warn if PR touches files that would invalidate memories
  pr-stale-check:
    if: github.event_name == 'pull_request'
    runs-on: ubuntu-latest
    permissions:
      pull-requests: write
    steps:
      - uses: actions/checkout@v4
        with:
          fetch-depth: 0

      - uses: actions/setup-node@v4
        with:
          node-version: '20'

      - name: install haive
        run: npm install -g @hiveai/cli

      - name: verify memory anchors touched by this PR
        id: verify
        run: |
          haive memory verify 2>&1 | tee /tmp/haive-verify.txt || true
          STALE=$(grep -c 'stale' /tmp/haive-verify.txt || echo 0)
          echo "stale_count=$STALE" >> "$GITHUB_OUTPUT"

      - name: comment on PR if stale memories detected
        if: steps.verify.outputs.stale_count != '0'
        uses: actions/github-script@v7
        with:
          script: |
            const fs = require('fs');
            const report = fs.readFileSync('/tmp/haive-verify.txt', 'utf8').trim();
            await github.rest.issues.createComment({
              owner: context.repo.owner,
              repo: context.repo.repo,
              issue_number: context.issue.number,
              body: \`### haive — Stale memories detected\\n\\nSome memories anchored to code modified in this PR may be outdated:\\n\\n\\\`\\\`\\\`\\n\${report}\\n\\\`\\\`\\\`\\n\\nRun \\\`haive memory verify --update\\\` locally to refresh them before merging.\`
            });
`;

export function registerInit(program: Command): void {
  program
    .command("init")
    .description("Initialize a hAIve project (.ai/ structure + bridge files)")
    .option("-d, --dir <dir>", "project root", process.cwd())
    .option("--no-bridges", "do not generate CLAUDE.md / .cursorrules / copilot-instructions.md")
    .option("--with-ci", "write a GitHub Actions workflow (.github/workflows/haive-sync.yml)")
    .action(async (opts: { dir: string; bridges: boolean; withCi?: boolean }) => {
      const root = path.resolve(opts.dir);
      const paths = resolveHaivePaths(root);

      if (existsSync(paths.haiveDir)) {
        ui.warn(`.ai/ already exists at ${paths.haiveDir} — leaving existing files in place.`);
      }

      await mkdir(paths.personalDir, { recursive: true });
      await mkdir(paths.teamDir, { recursive: true });
      await mkdir(paths.moduleDir, { recursive: true });
      await mkdir(paths.modulesContextDir, { recursive: true });

      if (!existsSync(paths.projectContext)) {
        await writeFile(paths.projectContext, PROJECT_CONTEXT_TEMPLATE, "utf8");
        ui.success(`Created ${path.relative(root, paths.projectContext)}`);
      }

      if (opts.bridges) {
        await writeBridge(root, "CLAUDE.md");
        await writeBridge(root, ".cursorrules");
        await writeBridge(root, path.join(".github", "copilot-instructions.md"));
      }

      if (opts.withCi) {
        const ciPath = path.join(root, ".github", "workflows", "haive-sync.yml");
        if (existsSync(ciPath)) {
          ui.info("CI workflow already exists — skipped");
        } else {
          await mkdir(path.dirname(ciPath), { recursive: true });
          await writeFile(ciPath, CI_WORKFLOW, "utf8");
          ui.success(`Created ${path.relative(root, ciPath)}`);
        }
      }

      ui.success(`hAIve initialized at ${root}`);
      console.log();
      console.log(ui.bold("Next steps:"));
      console.log(
        ui.dim("  1. Fill project context — let your AI agent do it:"),
      );
      console.log(
        "     " + ui.bold("In your AI client (Claude, Cursor…), invoke the MCP prompt: bootstrap_project"),
      );
      console.log(
        ui.dim("     This analyzes your codebase and writes .ai/project-context.md automatically."),
      );
      console.log(
        ui.dim("     Without this step, get_briefing returns a blank template (little value)."),
      );
      console.log();
      console.log(ui.dim("  2. Point your AI client at the MCP server:"));
      console.log(
        `     haive-mcp --root ${root}`,
      );
      console.log();
      console.log(ui.dim("  3. Start every AI session with:"));
      console.log("     " + ui.bold("get_briefing({ task: '…what you are about to do…' })"));
    });
}

async function writeBridge(root: string, relPath: string): Promise<void> {
  const target = path.join(root, relPath);
  if (existsSync(target)) {
    ui.info(`Bridge ${relPath} already exists — skipped`);
    return;
  }
  await mkdir(path.dirname(target), { recursive: true });
  await writeFile(target, BRIDGE_BODY, "utf8");
  ui.success(`Created bridge ${relPath}`);
}

